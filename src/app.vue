<script>
import { mapGetters } from 'vuex';

export default {
    computed: {
        ...mapGetters({
            count: 'getCount'
        })
    },
    created () {
        // 调用API从本地缓存中获取数据
        const logs = wx.getStorageSync('logs') || []
        logs.unshift(Date.now())
        wx.setStorageSync('logs', logs)

        console.log('app created and cache logs by setStorageSync', this.count)
    }
}
</script>

